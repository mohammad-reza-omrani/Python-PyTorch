# Import the torch module, which is the PyTorch library used for tensor operations and deep learning.
import torch

# Print a 2x2 tensor with random values generated from a uniform distribution between 0 and 1.
# The 'torch.rand()' function creates a tensor of the specified size with random values.
print(torch.rand(2, 2))  # Generates a 2x2 matrix with random values and prints it to the console.
